void mot_alea5(char *ch)
{
	int indice_mot = rand() % sizeof(mots) / sizeof(mots[0]);
	char *mot = mots[indice_mot];
	int i;
	for (i = 0; mot[i] != '\0'; i = i + 1)
		ch[i] = mot[i];
	ch[i] = '\0';
}

int est_dans_liste_mots(const char *ch)
{
	unsigned i;
	for (i = 0; i < 5; i = i + 1) {
		if (!isupper(ch[i]))
			return 0;
	}
	if (ch[5] != '\0')
		return 0;
	for (i = 0; i < sizeof(mots)/sizeof(mots[0]); i = i + 1)
		if (strcmp(ch, mots[i]) == 0)
			return 1;
	return 0;
}
